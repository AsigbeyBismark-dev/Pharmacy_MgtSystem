    private void txtUserNameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUserNameKeyReleased
        // TODO add your handling code here:
        String user = txtUserName.getText();
        if (!user.equals("")) {
            iconLabel.setVisible(true);
            iconLabel.setIcon(new ImageIcon("src\\icons\\done_20px.png"));
            iconLabel.setText("");
            checkUserName = 0;

            try {

                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3308/Pharmacy", "root", "2019");
                Statement st = con.createStatement();
                ResultSet rs = st.executeQuery("Select * from Users WHERE UserName='" +user+ "'");
                while (rs.next()) {
                    checkUserName = 1;
                    iconLabel.setIcon(new ImageIcon("src\\icons\\close_20px.png"));
                    iconLabel.setText("");

                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        } else {
            iconLabel.setVisible(false);
        }
    }//GEN-LAST:event_txtUserNameKeyReleased

