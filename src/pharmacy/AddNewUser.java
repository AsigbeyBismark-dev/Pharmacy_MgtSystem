    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        // TODO add your handling code here:
        String Usrole = (String) userRole.getSelectedItem();
        String FName = FullName.getText();
        SimpleDateFormat dformat = new SimpleDateFormat("dd-MM-yy");
        Date date = jDateChooser1.getDate();
        String dob = "";
        if (date != null) {
            dob = dformat.format(jDateChooser1.getDate());
        }
        String MobNumber = Mnumber.getText();
        String pass = Password.getText();
        String add = Address.getText();
        String username = txtUserName.getText();

        if (username.equals("") || FName.equals("") || MobNumber.equals("") || pass.equals("") || add.equals("")
                || dob.equals("")) {
            JOptionPane.showMessageDialog(null, "Please Enter all fields");
        } else if (!MobNumber.matches(MobileNumberPattern) || MobNumber.length() != 10) {
            JOptionPane.showMessageDialog(null, "mobile number is invalid");
        } else if (checkUserName == 1) {
            JOptionPane.showMessageDialog(null, "UserName already exist");
        }else{
            try{
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3308/Pharmacy", "root", "2019");
            //   Statement stm = (Statement)con.createStatement();
            String sql = "Insert into Users(UserName,Role,Password,FullName,Contact,Address, Date)values(?,?,?,?,?,?,?)";

            PreparedStatement pstm = con.prepareStatement(sql);
            pstm.setString(1, username);
            pstm.setString(2, Usrole);
            pstm.setString(3, pass);
            pstm.setString(4, FName);
            pstm.setString(5, MobNumber);
            pstm.setString(6, add);
            pstm.setString(7, dob);
           pstm.executeUpdate();
           JOptionPane.showMessageDialog(null, "User Added Successfully");
           setVisible(false);
           new AddNewUser().setVisible(true);
            
            }
            
                catch(Exception ex){
                       JOptionPane.showMessageDialog(null, ex);
                        }
            }
        

    }//GEN-LAST:event_SaveActionPerformed

