    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
     
        // TODO add your handling code here:
        
    
        String FName = FullName.getText();
               String MobNumber = Mnumber.getText();
        String pass = Password.getText();
        String add = Address.getText();
       

        if (FName.equals("")) {
            JOptionPane.showMessageDialog(null, "Name field is required");

        } else if (MobNumber.equals("")) {
            JOptionPane.showMessageDialog(null, "Contact field is required");

        } else if (pass.equals("")) {
            JOptionPane.showMessageDialog(null, "Password field is required");

        } else if (add.equals("")) {
            JOptionPane.showMessageDialog(null, "Address field is required");

        } else if (!MobNumber.matches(MobileNumberPattern) || MobNumber.length() != 10) {
            JOptionPane.showMessageDialog(null, "mobile number is invalid");

        } else {
            try {

                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3308/Pharmacy", "root", "2019");
                //   Statement stm = (Statement)con.createStatement();
                String sql = "Update Users SET FullName=? ,Password=?,Address=?,Contact=? ";

                PreparedStatement pstm = con.prepareStatement(sql);
                pstm.setString(1, FName);
                pstm.setString(2, pass);
                pstm.setString(3, add);
                pstm.setString(4, MobNumber);
               

                pstm.executeUpdate();
                JOptionPane.showMessageDialog(null, "Profile Updated Successfully");
                
                FullName.setText("");
                Password.setText("");
                Address.setText("");
                Mnumber.setText("");

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
  
    }//GEN-LAST:event_btnUpdateActionPerformed

