    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        try {

            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3308/Pharmacy", "root", "2019");
            Statement stm = con.createStatement();
            String sql = "SELECT FullName,Contact,Address,Password,UserName from Users";
            PreparedStatement pstm = con.prepareStatement(sql);

            ResultSet rs = pstm.executeQuery();
            
            while(rs.next()){
                FullName.setText(rs.getString("FullName"));
                Mnumber.setText(rs.getString("Contact"));
                Address.setText(rs.getString("Address"));
                Password.setText(rs.getString("Password"));
                jLabel2.setText(rs.getString("UserName"));
          
                
            }
        }
            catch(Exception ex){
                JOptionPane.showMessageDialog(null, ex);
            }
    }//GEN-LAST:event_formComponentShown

